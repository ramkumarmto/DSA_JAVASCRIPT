// Objects
// unordered key value pair
// when to use object ?
// when you have to do fast access,insertion and removal and no order required.
// insertion  O(1), removal O(1), access O(1) and searching O(n) 

// Object.keys -   O(N)

// Object.values -   O(N)

// Object.entries -   O(N)

// hasOwnProperty -   O(1)

//Arrays
//ordered list
// when to use ?
// don't need order
// fast insertion, removal and access

// bigO of arrays 
// Insertion -   It depends.... (it is from beginning or at end) push - O(1) and shift- O(n)

// Removal -   It depends.... (it is from beginning or at end) pop - O(1) and unshift - O(n)

// Searching -   O(N)

// Access -   O(1)

// concat, slice, splice - O(n)

// sort - nlog(n)

// map, filter, forEach, reduce etc having O(n)